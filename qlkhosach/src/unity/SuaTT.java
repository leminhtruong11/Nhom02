    public void loadInfor() {
        ten.setText(a.getTenNV());
        diachi.setText(a.getDiaChi());
        mail.setText(a.getMail());
        sdt.setText(a.getSDT());
        String pattern = "yyyy-MM-dd";
        DateFormat df = new SimpleDateFormat(pattern);
        String date = df.format(a.getNgaySinh());
        ngaysinh.setText(date);
        if(a.getGioiTinh()==1) gioitinh.setSelectedItem("Nam");
        else gioitinh.setSelectedItem("Nữ");
    }
   
    
