    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        if(checkNull()) {
            try{
                        String sqlChange="UPDATE NHANVIEN SET TenNV=?, SDT=?, Mail=?, GioiTinh=?, NgaySinh=?, DiaChi=? WHERE MaNV='"+this.begin.getText()+"'";
                        Connection conn=DBConnect.getConnection();
                        PreparedStatement pst=conn.prepareCall(sqlChange);
                        pst.setString(1, ten.getText());                        
                        pst.setString(2, sdt.getText());
                        pst.setString(3, mail.getText());
                        if((String)gioitinh.getSelectedItem()=="Nam") pst.setInt(4, 1);
                        else pst.setInt(4, 0);
                        pst.setString(6, diachi.getText());
                        
                       pst.setDate(5, java.sql.Date.valueOf(ngaysinh.getText()));
                        pst.executeUpdate();

                        status.setText("Đổi thông tin thành công!");
                        CompletableFuture.delayedExecutor(1, TimeUnit.SECONDS).execute(() -> {
                            status.setText("Trạng thái");
                        });

        }
        catch(Exception ex){
            ex.printStackTrace();
        }
            a.setInfor(begin.getText());
            loadInfor();
        } else {
            status.setText("Không được để trống thông tin");
                        CompletableFuture.delayedExecutor(1, TimeUnit.SECONDS).execute(() -> {
                            status.setText("Trạng thái");
                        });
        }
    }//GEN-LAST:event_jButton4ActionPerformed

